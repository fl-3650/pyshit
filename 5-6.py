import math


def f(x, y):
    total = 0
    n = len(x)
    x.insert(0, 0)
    y.insert(0, 0)

    for i in range(1, n + 1):
        total += math.sin(40 - y[n + 1 - math.ceil(i / 4)] ** 3 - x[n + 1 - i] ** 2) ** 4

    return total


print(f([0.2, 0.02, 0.25, 0.62, 0.09, 0.58, 0.69, 0.37],
        [-0.56, -0.67, -0.1, -0.32, 0.38, -0.17, -0.46, -0.87]))

print(f([0.25, 0.49, 0.57, -0.83, -0.7, 0.24, -0.31, 0.1],
        [0.19, 0.76, 0.73, -0.6, 0.77, 0.25, -0.44, -0.64]))

print(f([-0.77, -0.29, 0.39, -0.84, 0.41, -0.72, 1.0, 0.2],
        [0.46, -0.09, 0.94, 0.74, -0.12, 0.19, 0.04, 0.78]))
